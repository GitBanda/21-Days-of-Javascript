// Example of Nested Arrays
let capitalsOfSouthAmerica = [
    'Brazil: Brazilia',
    'Argentina: Buenos Aires',
    ['Bolivia: La Paz', 'Sucre'],
    'Chile: Santiago'
  ];

  console.log(capitalsOfSouthAmerica[2][1]); // Output: The second capital of Bolivia, Sucre, gets output
  console.log(capitalsOfSouthAmerica[2]);    // Output: The two capitals of Bolivia, La Paz and Sucre, gets printed out.
  